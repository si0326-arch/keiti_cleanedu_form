// 소속 처·단의 1순위 부패위험에 맞는 추천 신고서 목록을 반환한다.
// 1순위 추천(처·단 맞춤) + 전사 공통 추천(특혜제공 부서 제외) 순으로 구성된다.
export function getFormRecommendations(deptId) {
  const dept = DEPARTMENTS.find((d) => d.id === deptId);
  if (!dept) return [];

  const recommendations = [];
  const scenarios = DEPT_SCENARIOS[deptId] || [];

  if (dept.primaryRisk === "특혜제공") {
    recommendations.push({
      formKey: "planning-main",
      formName: "사적이해관계자 신고·회피 신청서",
      formDesc: "법적신고용 + KEITI제도용",
      reason: `${dept.name}의 1순위 부패위험 '특혜제공'에 대응`,
      isPrimary: true,
      scenarios,
    });
  } else if (dept.primaryRisk === "갑질행위") {
    recommendations.push({
      formKey: "green-defense",
      formName: "공정직무 저해 지시 소명서",
      formDesc: "부당지시 거부 소명",
      reason: `${dept.name}의 1순위 부패위험 '갑질행위'에 대응`,
      isPrimary: true,
      scenarios,
      formIdOverride: "fairDutyDefense",
    });
  } else if (dept.primaryRisk === "부정청탁") {
    recommendations.push({
      formKey: "selfReport-main",
      formName: "신고서(자진 신고용)",
      formDesc: "청탁금지법 위반 자진 신고",
      reason: `${dept.name}의 1순위 부패위험 '부정청탁'에 대응`,
      isPrimary: true,
      scenarios,
    });
  } else if (dept.primaryRisk === "사익추구") {
    recommendations.push({
      formKey: "conflictViolation-main",
      formName: "이해충돌방지법 위반행위 신고서",
      formDesc: "이충법 위반행위 신고",
      reason: `${dept.name}의 1순위 부패위험 '사익추구'에 대응`,
      isPrimary: true,
      scenarios,
    });
  }

  if (dept.primaryRisk !== "특혜제공") {
    recommendations.push({
      formKey: "common-conflict",
      formName: "사적이해관계자 신고·회피 신청서",
      formDesc: "법적신고용 + KEITI제도용 (전사 공통)",
      reason: "전사 공통 1순위 '사익추구' 대응",
      isPrimary: false,
      scenarios: [COMMON_SCENARIOS.legal, COMMON_SCENARIOS.keiti],
    });
  }

  return recommendations;
}
